	  function change() {
		  if ( elemIDmain.style.color == 'black') {
	  
	  	  elemIDmain.style.fontFamily = 'Georgia'; 
	      
	  	  elemIDmain.style.fontSize = '16px';
	  	  elemIDmain.style.color = 'red';
	  
       } else 
		   
		   {
			 elemIDmain.style.fontFamily = 'verdana'; 
	     
	  	  elemIDmain.style.fontSize = '12px';
	  	  elemIDmain.style.color = 'black';
		   }
	  }
 
	